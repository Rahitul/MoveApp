package com.example.wub_project;

public class ReceiptData {
    private String showNumber;
    private String showCost;
    private String showCostWithoutDis;

    public ReceiptData() {
    }

    public String getShowNumber() {
        return showNumber;
    }

    public void setShowNumber(String showNumber) {
        this.showNumber = showNumber;
    }

    public String getShowCost() {
        return showCost;
    }

    public void setShowCost(String showCost) {
        this.showCost = showCost;
    }

    public String getShowCostWithoutDis() {
        return showCostWithoutDis;
    }

    public void setShowCostWithoutDis(String showCostWithoutDis) {
        this.showCostWithoutDis = showCostWithoutDis;
    }
}
